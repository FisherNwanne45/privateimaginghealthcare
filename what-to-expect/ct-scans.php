            <p dir="ltr"><strong>What are CT Scans used for?</strong></p>
            <p>CT scans detect soft tissue, bone and joint problems, such as complex bone fractures, fluid
                collections and tumors.&nbsp;</p>
            <p>If you have a condition like cancer, heart disease, emphysema, or liver masses, CT scans can spot
                it or help doctors see any changes. They allow clinicians to visualize internal injuries, like
                bleeding, and can also help locate a tumor, diagnose a blood clot and identify excess fluid, or
                infection.&nbsp;</p>
            <p>Healthcare providers utilize CT scans to guide treatment plans and procedures, such as biopsies,
                surgeries, and radiation therapy. In addition, physicians can compare CT scans to find out if
                certain treatments are working. For example, scans of a tumor can determine whether it&rsquo;s
                responding to chemotherapy or radiation.</p>
            <p>&nbsp;</p>
            <p><strong>Preparing for your CT Scan</strong></p>
            <p>Your physician will give you instructions before your appointment outlining what you need to do
                to prepare for your CT scan. Depending on the type of CT scan you will have (which part of the
                body and type of scan) preparation may begin up to 24 hours before your scan.&nbsp;</p>
            <p>Note: If your scan requires Contrast Materials you will be told to drink only clear liquids 3
                hours prior to your scan and may be required to drink oral contrast at home 1.5 hour prior to
                your scan. In addition, we want to ensure you are hydrated for your test.&nbsp;&nbsp;</p>
            <h2 dir="ltr"><span>How are CT Scans performed?</span></h2>
            <p dir="ltr"><span>Before the scan, you will be asked to remove any metal objects, such as jewelry,
                    zippers or buttons. Then, a licensed Radiology Technologist will give you detailed
                    instructions and perform your CT scan.&nbsp;</span></p>
            <p>You will lay on a table inside a large, doughnut-shaped CT machine during the test. X-rays
                will&nbsp; rotate 360 degrees around your body as the table slowly moves through the scanner.
                Movement can blur the images, so you&rsquo;ll be asked to stay very still during the test. You
                may need to hold your breath at times. Breathe hold times can vary depending on what body parts
                are being scanned - however, it is typically only for a few seconds. CT scans are performed
                relatively quickly. The entire scan process can take anywhere from a few minutes up to a
                half-hour.&nbsp;</p>
            <p><strong>What is a CT Scan with Contrast Materials?</strong></p>
            <p>Your doctor may want your CT scan to be with Intravenous (IV) or Oral Contrast Materials.
                Contrast Material is a special dye that helps soft tissues appear clearly during your CT Scan.
                The material appears white on the scan, highlighting <a
                    href="https://www.webmd.com/a-to-z-guides/rm-quiz-blood-basics">blood</a> vessels,
                intestines, organs, or other structures in your body.&nbsp;</p>
            <p>Contrast Material can be given orally prior to your test, and/or through an IV during your test.
                If a test is ordered with IV Contrast Material, our nurses will place an IV catheter in your arm
                prior to your scan. It is normal to feel a warm sensation or a metallic taste in your mouth
                during your test with IV Contrast Material. This sensation only lasts a few seconds - if at
                all.&nbsp;</p>
            <p>Our Nursing and Technologist staff will be with you through the entire process ensuring you are
                as comfortable as possible.&nbsp;</p>
            <p>After your CT scan, you&rsquo;ll need to drink plenty of fluids to help your <a
                    href="https://www.webmd.com/urinary-incontinence-oab/picture-of-the-kidneys">kidneys</a>
                remove the contrast material from your body. You can continue your normal diet after your
                scan.&nbsp;</p>
            <p><strong>Let us know of any Medical Conditions</strong></p>
            <p><span id="docs-internal-guid-b338ee30-7fff-d78d-e9a0-1444e33a7a16">Please let us know if you have
                    any medical conditions, especially if you have any allergy to iodinated Contrast Material or
                    had a reaction to Contrast Material during a CT Scan in the past. It is extremely important
                    we are aware right away, before we book your appointment. It is also important to know if
                    you have any kidney issues - such as chronic kidney disease or if you are on dialysis. In
