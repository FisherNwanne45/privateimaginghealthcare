        <h2>How It Works</h2>
        <p>PET/CT scanning works by first using radiotracers. These are molecules that are linked to small
            amounts of radioactive material detectable on the PET scan.&nbsp; Radiotracer accumulates in
            cancerous tumors as well in the areas of inflammation. F-18 fluorodeoxyglucose or FDG is the most
            commonly-used radiotracer; a molecule similar to glucose.&nbsp; Cancer cells absorb glucose at a
            higher rate which can be detected on PET/CT scans.</p>
        <p>The radiotracer is injected into the body through intravenous access or port. It will accumulate in
            the area of the body that is subject to examination. The radiotracer&rsquo;s emissions are detected
            by a PET/CT scanner which produces images and provides molecular information. &nbsp;Those images are
            then reconstructed using a PET algorithms to produce detailed slices offering insights of the
            structure and function of organs and tissues in the body.</p>
        <h3>During The Test</h3>
        <p>PET/CAT scanning is a non-invasive procedure that can take up to two hours.&nbsp; It is typically
            undertaken on an outpatient basis.</p>
        <p>The test begins by obtaining blood sugar level to ensure it is within PET scan parameters. Nursing
            staff will place a small needle into a vein, usually in your arm or the back of your hand, to fit an
            intravenous line (a thin plastic tube) through which the liquid radioactive material is injected. If
            needed Port-A-Cath can be accessed. You will be asked to rest quietly on a recliner, avoiding
            movement or talking for approximately one&nbsp;hour. &nbsp;During this time you will be alone.</p>
        <p>The patient will be instructed to empty their bladder to improve image quality. You will then be
            moved to the scanning room and positioned on the PET scanning bed. The CT scan is done first and
            takes less than 2&nbsp;minutes. The PET scan takes approximately 25&nbsp;minutes, but the time will
            vary depending on the areas of your body being scanned.</p>
        <p>The intravenous line will be removed before you leave. You should drink plenty of fluids after the
            test is finished. This will flush the radioactive substance out of the body through the kidneys and
            into the bladder.</p>
        <h3>Preparation</h3>
        <p>If you have had a prior PET/CT and or CT scan at another facility, you must bring a copy of that disc
            or discs. Please notify your physician if you are claustrophobic or pregnant. Plan to be in the
            PET/CT department for approximately 2 to 3hrs and please note that we advise patients to stay away
            from children and pregnant women for 6 to 24 hours following the time of injection. We are extremely
            cautious in this regard; there are NO definite risks from this level of radiation.</p>
        <p>Please wear comfortable and warm clothing to the exam and do not wear jewelry or clothes with any
            metal buttons, underwire bra, or metal zippers.</p>
        <h4>Diabetic Patients:</h4>
        <ul>
            <li>Insulin Controlled Diabetes: Have a meal with your insulin 4 hours prior to your appointment
            </li>
            <li>Non-Insulin Controlled Diabetes:
                <ul>
                    <li>If you have a morning PET/CT appointment, please do not take your diabetic tablets on
                        the morning of your scan but bring them with you so that you can take them immediately
                        after your scan has been completed.</li>
                    <li>If you have an afternoon PET/CT appointment, please take your diabetic tablets on the
                        morning of your scan, not less than 6 hours before the time of your appointment.</li>
                </ul>
            </li>
        </ul>
        <h4>PET/CT Diet Plan:</h4>
        <p>24 Hours before your exam &ndash;</p>
