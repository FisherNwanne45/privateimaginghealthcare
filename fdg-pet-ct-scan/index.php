        </ul>
        <p>6 Hours before your exam &ndash;</p>
        <ul class="col-2">
            <li>Do NOT eat(including tube feeding)</li>
            <li>Do NOT chew gum or cough drops, etc</li>
            <li>Drink ONLY water without additives</li>
            <li>No smoking</li>
            <li>Take all prescribed non-diabetic medications.</li>
        </ul>
