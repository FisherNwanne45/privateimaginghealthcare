                <h2>Overview</h2>
                <p>PET/CT scan is a type of procedure that makes use of images from two technologies in
                    determining the exact location of an abnormality in the body and consequently, detecting
                    diseases such as cancer and other abnormalities.</p>
                <p>Positron emission tomography (PET) uses controlled amounts of a radioactive substance, called
                    a radiotracer, in measuring cell activity in an organ or tissue.</p>
                <p>Computed tomography (CT) imaging uses special x-ray equipment to produce multiple images or
                    pictures of the inside of the body. CT imaging provides excellent anatomic information.</p>
                <p>By combining information about the body&rsquo;s anatomy and metabolic function, a PET/CT scan
                    provides a more detailed picture of cancerous tissues than either test does alone. The
                    images are captured in a single scan, which provides a high level of accuracy.</p>
                <p>Aside from detection, a PET/CT scan can also determine whether the disease has spread to
                    other parts of the body. It can also evaluate the effectiveness of a cancer treatment plan
                    or determine if the disease has returned after treatment.</p>
