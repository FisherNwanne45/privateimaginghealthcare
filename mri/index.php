                    diagnosis.</p>
                <h2>How It Works</h2>
                <p>Most MRI machines are large, cylindrical magnets. You are asked to lie down on a motorized
                    bed and once comfortable, the bed slides inside the cylindrical structure, which is open at
                    both ends. A computer, operated by a technologist, is used to manage the scanning procedure
                    which is located in an adjacent room to keep it away from the magnetic field generated by
                    the scanner. The technologist can see and converse with you from the other room. An MRI
                    usually can take 10-30 minutes, depending upon specific requirements. You are allowed to
                    have a family member or a friend with you during the exam.</p>
