        <p dir="ltr"><strong>During your procedure<br /></strong>The area where the implanted port will be
            placed will be cleaned and numbed with local anesthetic (lidocaine/novocaine).&nbsp;</p>
        <p>Access to a vein at the base of your neck will be obtained. A small incision will be made on your
