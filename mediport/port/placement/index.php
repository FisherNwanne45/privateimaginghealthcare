            ski, and threaded into your vein.<br />Your incisions will be closed with either sutures (stitches)
            or surgical glue called Dermabond&reg;. If you have sutures, they will be absorbed and won&rsquo;t
            need to be removed.</p>
        <p dir="ltr"><strong>After your procedure<br /></strong>You will stay in our recovery area for
            approximately 1 hour to make sure you are fully awake and feeling well following the procedure.</p>
        <p>You may have some discomfort at your incision sites and where the catheter was tunneled under your
            skin. This should get better in 24 to 48 hours. You can take over-the-counter pain medication if you
            need it. Most people don&rsquo;t need any pain medication.</p>
        <p dir="ltr"><strong>Caring for your port site<br /></strong>Do not get the area wet for 24 hours. After
            24 hours, you can shower, but should still avoid getting the site wet. You should not take a bath or
            swim for at least 1 week.</p>
        <p>For 5 days after your implanted port is placed, don&rsquo;t lift anything heavier than 10 pounds and
            do not engage in any rigorous exercise. You may do usual light activities after 24 hours.</p>
