        <h3>How It Works</h3>
        <p>Ultrasound operates under the same principles originally developed to track submarines, allowing for
            non-invasive imaging that does not expose a patient to any amount of radiation. Ultrasound makes use
            of a small device called a transducer that is placed on the skin and emits high-frequency sound
            waves that pulse throughout a patient&rsquo;s body.</p>
        <p>These pulses &ldquo;echo&rdquo; and are received by the transducer where the information is sent to a
            computer, where it is analyzed and converted into images. A real-time picture is then displayed on a
            monitor. This helps in examining blood flow and determining the characteristics of organs and
            tissues such as shape and size. It can also be useful in detecting abnormal masses like tumors.</p>
        <h3>During The Test</h3>
        <p>Patients are advised to wear comfortable and loose-fitting clothes for the exam. Depending on the
            area being examined, patients may be asked to wear a hospital gown during the procedure.</p>
        <p>The patient will lie face up on a padded examination table. It is possible that the patient may be
            asked to turn to either side in order to enhance the quality of the images. The technologist will
            apply a small amount of gel on the part of the body that is to be examined. The gel is warm and
            water-based and thus can be easily wiped off after the procedure. It is specifically designed to
            prevent any air from getting between the skin and the transducer so that the latter can deliver
            sound waves into the body. The technologist may also change the direction or angle of the transducer
            to acquire the best possible image of the body part being examined. Once the exam is complete, the
            clear gel will be wiped off. On average, an ultrasound scan can last for 15 to 20 minutes.</p>
