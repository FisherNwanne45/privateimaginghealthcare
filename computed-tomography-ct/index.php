        <h1>COMPUTED TOMOGRAPHY</h1>
        <br>
        <p><img src="../media/assets/Untitled-1_NYImaging.jpg" alt="" width="100%" /></p>
        <div class="row">
            <div class="col-lg-8">
                <p>Computer tomography scan or more popularly known as CT scans are a non-invasive diagnostic
                    imaging procedure utilizing a combination of computer and X-ray technology to generate
                    accurate and detailed images of body structures including organs, blood vessels, tissues,
                    and bones. Widely regarded as one of the most important medical developments of the past
                    century, it is a valuable medical diagnostic tool that allows specialists to more easily and
                    accurately diagnose diseases.</p>
                <p>Compared to the standard X-ray technology which generates two-dimensional views of bodily
                    structures, CT scans can create detailed and 360-degree images. This is made possible by an
                    X-ray machine rotating around the body and taking various images in thin and horizontal
                    slices. A computer, in turn, draws together these cross-section images to create a
                    three-dimensional model of the bodily structure.</p>
            </div>
            <div class="col-lg-4">
                <div class="call-to-action call-to-action-colored">
                    <h3>What To Expect: CT Scans</h3>
                    <p>More information and instructions for this procedure.</p>
                    <a class="btn btn-outline btn-light" href="../what-to-expect/ct-scans.php">Learn More</a>
                </div>
            </div>
        </div>
        <p>CT Scans are typically utilized in the workup of abdominal or chest pain. During cases when cancer is
            suspected, a CT scan is relied upon by physicians in visualizing the tumor and assessing its size
            and location as well as the potential involvement with other tissues.</p>
        <p>CT scanning is also widely used for the early detection of cancer. Low-dose CT scans, wherein the
            radiation emitted is significantly lower and which can take less than a minute to complete, is
            highly recommended for adults considered to be at high-risk of lung cancer due to their history of
            smoking.&nbsp; It may also be used in detecting vascular diseases, osteoporosis, infections, finding
            injuries, clots, hemorrhages, and other issues.</p>
        <h2>How It Works</h2>
        <p>During a CT scan procedure, a patient lies on a motorized table that goes through the CT scanner, a
            large machine with a hole in the center. This advanced machine emits a series of radiation beams
            through the patient&rsquo;s body which is held in position with straps and pillows.</p>
        <p>As the table moves the patient into the scanner, an X-ray source rotates around the circular opening.
            Each rotation yields different images of internal organs and tissues. All data coming from the X-ray
            source is digitally transmitted to a computer that reconstructs the individual shots of internal
            organs and tissues.</p>
        <p>In some instances, a patient may need to be administered with a contrast agent to visualize organs,
            blood vessels, and certain structures that are hard to see. A contrast agent may be administered
            intravenously, orally, or both; depending on how your doctor prescribes it.</p>
        <h3>During The Test</h3>
        <p>During the test, the patient will lie on the motorized table which then goes through the CT scanner.
            The specialist leaves the exam room and goes into a control room. The patient can communicate with
            the technologist through an intercom.</p>
        <p>The table then slowly goes into the scanner and moves a few millimeters at a time. Buzzing and
            whirring sounds may be heard. However, the patient won&rsquo;t feel anything throughout the entire
            procedure.</p>
        <p>The patient needs to stay still while the CT images are being captured. Movement can result in
            blurred images. In some instances, the patient may be asked to hold his or her breath for a few
            seconds to prevent the chest&nbsp;<span>and abdomen&nbsp;</span>from moving.</p>
        <p>Once the procedure is over, the captured images are processed and transmitted to a radiologist for
            review. A report will be generated and sent electronically to your chart and to your ordering
            physician.</p>
        <p>CT scans are a painless procedure that normally takes 15-20 minutes to complete including the
            interview and administration of contrast material.&nbsp; But there are instances when additional
            scanning may be needed depending on the diagnostic requirements.</p>
        <h3>Preparation</h3>
        <p>Regardless of the type of scan a patient may receive, it is very important that the patient avoids
            clothing with any metal zippers, underwire bras, metal clasps, or jewelry.&nbsp; Also, please notify
            the ordering physician if there is any chance that you may be pregnant.</p>
        <h4>For Patients Drinking Oral Contrast:</h4>
        <ul>
            <li>Three hours of fasting prior to the exam&nbsp;<span>(clear liquids are acceptable)</span>.</li>
            <li>Please inform your physician if you have any allergy to any contrast agent or had an allergic
                reaction in the past.</li>
            <li>On&nbsp;the day of&nbsp; your CT Scan, please do the following:<ol>
                    <li>Pour the 30mL bottle of Gastroview into the 500mL bottle of water (shake well before
                        drinking).</li>
                    <li>Start drinking the mixture 1.5 hours prior to your appointment time, but do not finish
                        it.</li>
                    <li>Bring the bottle with you to the appointment and finish drinking it in the waiting room.
                    </li>
                </ol>
            </li>
        </ul>
        <h4>For Patients Receiving An IV Contrast:</h4>
        <ul>
            <li>Three hours of fasting prior to the exam&nbsp;<span>(clear liquids are acceptable)</span>.</li>
            <li>If you are receiving IV contrast for a CT scan, you must have a&nbsp; CREATININE blood test
                drawn and the results forwarded to us or in your chart prior to the CT scan. Please inform your
                physician if you have any allergy to any IV contrast agent or had an allergic reaction in the
                past.<ol>
                    <li><span>Kidney function blood work&nbsp;</span>is required for all contrast patients
                        within 30 days.</li>
                    <li>You will be asked to fill out a questionnaire and consent form for injection.</li>
                </ol>
            </li>
        </ul>
