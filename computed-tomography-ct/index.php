        <h2><strong>Types Of CT Scans</strong></h2>
        <ul>
            <li><span>CT Head or Brain.&nbsp;</span>CT scan of the brain is used to assess injury or abnormality
                including tumors, bleeding, blood clots, or any sign of stroke.</li>
            <li><span>CT Orbits.&nbsp;</span>This is a procedure that scans the eyes, its sockets or orbits, as
                well as the surrounding nerves, muscles, and bones. It is used to detect infection of the eye
                area, foreign object in the eye socket or a damaged eye socket bone.</li>
            <li><span>CT Soft Tissue Neck.&nbsp;</span>This type of CT scan examines the neck starting from
                above the ears and below the collarbone. It can accurately diagnose conditions affecting areas
                like the mouth, throat, nasal passages, and thyroid glands.</li>
            <li><span>CT Maxillofacial.&nbsp;</span>This is a key diagnostic procedure for evaluating face
                trauma. It is used to assess parts of the face like the jaw, orbits, sinuses, and cheekbone.
            </li>
            <li><span>CT Sinus.&nbsp;</span>A procedure aimed at capturing images of the sinuses, CT sinus is
                useful in diagnosing sinusitis and examining tumors of the sinuses and the nasal cavity.</li>
            <li><span>CT Chest.&nbsp;</span>This procedure helps uncover the cause of unexplained chest pain,
                cough, difficulty in breathing, and other chest-related symptoms.</li>
            <li><span>CT Abdomen.&nbsp;</span>This examination helps in detecting diseases of the colon, small
                bowel, and other internal organs.</li>
            <li><span>CT Pelvis.&nbsp;</span>CT pelvis examines structures inside and near the pelvis such as
                bladder, prostate and reproductive organs as well as pelvic bones and lymph nodes.</li>
            <li><span>Cervical&nbsp;</span><span>Spine</span><span>.&nbsp;</span>This procedure is often
                performed in patients who recently were in an accident as well as those who are complaining of
                neck pain. It examines the portion of the spine running through the neck.</li>
            <li><span>Thoracic&nbsp;</span>This type of CT scan can diagnose abnormalities in the mid back area
                from the bottom of the neck to the upper abdominal area<span>.</span></li>
            <li><span>Lumbar&nbsp;</span><span>Spine</span><span>.&nbsp;</span>This type of CT scan can diagnose
                conditions affecting the lumbar portion of the spine.</li>
            <li><span>CT Upper Extremity.&nbsp;</span>This procedure can evaluate pain, swelling, trauma, and
                fracture in any area of the upper extremity including the hand, shoulder, and arms.</li>
            <li><span>CT Lower Extremity.&nbsp;</span>By obtaining multiple images of the hip, ankle, knees, and
                foot, this procedure can help determine the cause of swelling, pain, or trauma involving the
                lower extremities.</li>
            <li><span>Low-Dose Lung Cancer Screening.&nbsp;</span>This type of CT scan is recommended for people
                with no history or symptoms of the disease but are considered high-risk because of a history of
                heavy smoking or advanced age (55 to 80 years old).</li>
        </ul>
