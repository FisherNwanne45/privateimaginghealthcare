        <h1>Hepatic Arterial Embolization</h1>
        <br>
        <p dir="ltr">Hepatic embolization blocks the blood flow to liver tumors. Your interventional radiologist
            will thread a small catheter (flexible tube) into your hepatic artery. Then, he will inject tiny
            particles into the catheter. These particles will block the blood flow to the tumor, which will help
            to kill it. We sometimes also inject chemotherapy or radioactive particles (Y-90) directly into the
            tumor.</p>
        <p><span><span>&nbsp;</span></span></p>
        <p dir="ltr"><strong>Before your procedure<br /></strong>Before the procedure, you will have an IV line
            placed in your arm. You may get medication through your IV that will make you feel drowsy. The
            medication will control pain and anxiety. You may need to have a urinary catheter placed at the
            beginning of the procedure.</p>
        <p><span><span>&nbsp;</span></span></p>
        <p dir="ltr"><strong>During your procedure<br /></strong>An artery in your groin or wrist will be chosen
            using ultrasound-guidance. The area will be cleaned and numbed with local anesthetic
            (lidocaine/novocaine).</p>
        <p>The interventional radiologist will guide the catheter into the hepatic artery using fluoroscopy
            (video x-ray). He will then use dye to confirm that the catheter is in the correct place. Once the
            catheter is in place, the interventional radiologist will inject the particles until he sees that
            the blood flow to the tumor has been adequately stopped or until he has administered the appropriate
            amount of chemotherapy or radioactive particles into the tumor.</p>
        <p>At the end of the procedure, the catheter will be removed and the site will be covered with a
            dressing.</p>
        <p><strong>After your procedure<br /></strong>You will stay in our recovery area for between 2 and 4
            hours to make sure you are fully awake and feeling well following the procedure. Additionally, if we
            access through an artery in your groin, you will need to lay down while you are in the recovery area
            and keep that leg straight.</p>
        <p><strong>At home<br /></strong>You may experience &ldquo;post-embolization syndrome,&rdquo; which can
            include the following symptoms:</p>
        <ul>
            <li dir="ltr">
                <p dir="ltr"><span>Nausea and/or vomiting&nbsp;&nbsp;</span></p>
            </li>
            <li dir="ltr">
                <p dir="ltr"><span>Pain is the most common side effect that can occur, because the blood supply
                        to the treated area is cut off</span></p>
            </li>
            <li dir="ltr">
                <p dir="ltr"><span>These side effects usually improve within 3 to 5 days and may be improved
                        with medication</span></p>
            </li>
            <li dir="ltr">
                <p dir="ltr"><span>You may also experience a low-grade fever, lethargy and fatigue that could
                        last up to 1 week</span></p>
            </li>
            <li dir="ltr">
                <p dir="ltr"><span>You should tell your doctor if these symptoms last more than 10 days</span>
                </p>
            </li>
        </ul>
        <p>Do not get the access site wet for 24 hours. After 24 hours, you can shower, but should still avoid
            getting the site wet. You should not take a bath or swim for at least 7 days.</p>
        <p>For 5 days after your procedure, don&rsquo;t lift anything heavier than 10 pounds and do not engage
            in any rigorous exercise.You may do usual light activities after 24 hours.<br />Special
            considerations for Y-90 (radioembolization)</p>
        <ul>
            <li dir="ltr">
                <p dir="ltr"><span>Do not attempt to become pregnant or get someone pregnant</span></p>
            </li>
            <li dir="ltr">
                <p dir="ltr"><span>Nursing mothers should not nurse</span></p>
            </li>
            <li dir="ltr">
