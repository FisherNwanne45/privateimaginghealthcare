                                 <h1>World Ovarian Cancer Day: Interventional Radiology for Gynecologic Malignancies</h1>
                                 <div class="post-meta">
                                     <span class="post-meta-date"><i class="fa fa-calendar-o"></i>May 5, 2023</span>
                                 </div>
                                 <p dir="ltr"><span>The role of interventional radiology in women&rsquo;s health has expanded significantly in recent years. Advances have been made both in terms of developing new treatments, as well as adapting existing treatments for conditions affecting women.</span></p>
                                 <p dir="ltr">Interventional radiology allows physicians to diagnose and treat patients while using the least invasive techniques. Their goal is to minimize risk to the patient and improve health outcomes. Interventional radiology is fundamental in the treatment of multiple conditions that affect women. This can be especially important for women with gynecologic malignancies. Gynecologic malignancies are the third most common group of cancers in women, the most common subtype of which is uterine cancer.</p>
                                 <p dir="ltr"><span>Interventional radiologists are able to place mediports for chemotherapy in a way which is minimally invasive.&nbsp; We provide sedation for all of our procedures. We are also able to offer locoregional therapy for gynecologic malignancies which have spread to the liver. The two types of locoregional therapies that we offer are embolization and ablation.</span></p>
                                 <p dir="ltr"><span>Hepatic arterial embolization blocks the blood flow to tumors in the liver. The interventional radiologist is able to thread a small catheter (flexible tube) into your hepatic artery. Then, they will inject tiny particles into the catheter. These particles will block the blood flow to the tumor, which will help to kill it. We sometimes also inject chemotherapy directly into the tumors.</span></p>
                                 <p dir="ltr"><span>Ablation is a technique that involves destroying the tumor using heat or cold energy. At New York Imaging, we most commonly use microwave ablation for treatment of metastatic gynecologic malignancies which have spread to the liver.&nbsp; Microwave ablation uses high-frequency electromagnetic waves to heat to destroy the tumor.</span></p>
                                 <p dir="ltr">&nbsp;In the past the primary treatment of gynecologic malignancies has typically involved only surgery and chemotherapy. This is all changing now with the involvement of interventional radiology.&nbsp;&nbsp;</p>
                                 <p dir="ltr"><span>The benefits of interventional radiology over traditional surgery option are:</span></p>
                                 <ul>
                                     <li dir="ltr">
                                         <p dir="ltr"><span>Shorter recovery time</span></p>
                                     </li>
                                     <li dir="ltr">
                                         <p dir="ltr"><span>Minimally invasive</span></p>
                                     </li>
                                     <li dir="ltr">
                                         <p dir="ltr"><span>Less expensive</span></p>
                                     </li>
                                     <li dir="ltr">
                                         <p dir="ltr"><span>No hospital stay</span></p>
                                     </li>
                                     <li dir="ltr">
                                         <p dir="ltr"><span>Fewer complications</span></p>
                                     </li>
                                 </ul>
                                 <p dir="ltr"><span>These treatments are also important because they open the door for patients who are not surgical candidates or are unable to tolerate aggressive systemic therapy. Speak to your physician about interventional radiology and if one of these procedures is right for you.</span></p>
