                                <p dir="ltr"><span>The interventional radiology (IR) team at New York Cancer
                                        &amp; Blood Specialists perform minimally invasive therapies using
                                        imaging to guide minimally invasive procedures that diagnose, treat, and
                                        cure many conditions. Interventional radiology procedures are often less
                                        expensive, less risky, and less painful than traditional surgery.&nbsp;
                                        Many patients can potentially benefit from these procedures but are
                                        unaware of the option.&nbsp;</span></p>
                                <p dir="ltr"><span>IR is one of the pillars of treatment for both primary and
                                        secondary tumors. A patient&rsquo;s first encounter with IR might be an
                                        image-guided biopsy, which uses ultrasound or CT to guide a needle
                                        directly into the tumor. Previously, biopsies required being cut open or
                                        laparoscopic surgery to take a piece of the liver. With IR, the whole
                                        biopsy procedure takes about ten minutes. The procedures can be
                                        performed with either sedation or local anesthesia. Patients leave the
                                        same day with a band-aid and no incisions.</span></p>
                                <p dir="ltr"><span>The two main types of IR procedures that treat cancers are
                                        ablation and embolization. Ablation is a needle-based procedure that
                                        uses heat or cold to destroy the tumors. At NYCBS, microwave ablation is
                                        performed using the latest and most advanced system, NEUWAVE Microwave
                                        Ablation System. The system allows for more precise and complete
                                        treatment of tumors. Similar to a biopsy, imaging is used to guide a
                                        needle into the tumor, but instead, this needle emits microwaves that
                                        heat the tumor, burn, and destroy it. Ablation procedures can, in
                                        certain instances, completely cure cancer, for example, stage 1 renal
                                        cell carcinoma and BCLC stage 0 and stage A Hepatocellular Carcinoma
                                        (HCC). Ablation is a good treatment option for patients who are not good
                                        surgical candidates or have such small tumors that they do not
                                        necessarily need more extensive surgery. Patients recover just as
                                        quickly from the ablation procedure as from biopsies. They are monitored
                                        for two hours and then go home.</span></p>
                                <p dir="ltr"><span>For patients with painful metastatic bone lesions, ablation
                                        is helpful not only for curing tumors but also as a palliative
                                        procedure. Although burning the painful bone tumors may not cure the
                                        cancer, it can help provide lasting pain relief. This can also be
                                        combined with external beam radiation for additional pain
                                        control.</span></p>
                                <p dir="ltr"><span>Embolization is another outpatient IR cancer treatment.
                                        Embolization blocks arterial blood flow to tumors using tiny particles
                                        injected through a catheter directly into the organ with the tumors. For
                                        example, primary liver cancer (HCC) is a very arterial vascular type of
                                        tumor that responds well to embolization. When treating one or few focal
                                        tumors, the interventional radiologist can get into the specific tiny
                                        vessel that feeds only that tumor (or at least primarily that tumor and
                                        just a small part of the surrounding liver). The normal liver gets its
                                        blood supply mainly from the portal vein. So even if the entire hepatic
                                        artery is blocked, the normal liver tissue still survives while the
                                        tumors die. For patients with certain types of diffuse metastatic liver
                                        disease, embolization of an entire lobe or even the entire liver can be
                                        performed. When embolizing the entire liver, treatment is usually
                                        performed over two separate sessions.&nbsp; This usually helps to shrink
                                        the tumors throughout the whole liver but leaves the normal liver tissue
                                        mostly intact.</span></p>
                                <p dir="ltr"><span>Unlike surgery, with embolization, patients have no or
                                        minimal scarring. Embolization often works part of the way and shrinks
                                        the tumors but does not always eliminate them. In those cases, we can
                                        repeat the procedures as often as needed to keep the tumors under
                                        control. For example, with primary cancer of the liver, Hepatocellular
                                        carcinoma (HCC), if it doesn't work the first time, we can repeat the
                                        procedure the following month. The goal is to cure the cancer in that
                                        case by repeating the process and being more aggressive until the tumor
                                        is destroyed. For patients with diffuse metastatic disease, the goal is
                                        tumor control rather than cure.&nbsp; These patients may experience
                                        regression or shrinking of the tumors, and then a year after treatment,
                                        the tumors may recur or progress.&nbsp; If the tumors recur or progress,
                                        embolization can be repeated as often as needed.&nbsp;</span></p>
                                <p dir="ltr"><span>Chemoembolization is a specific type of arterial embolization
                                        in which particles are mixed with chemotherapy that is also injected
                                        into the tumors. That allows the tumor to be hit with 50 times as much
                                        chemo as during a regular chemo infusion, but the patient's body will
                                        still get less than a regular chemo infusion. As a result, patients
                                        don't experience the chemo side effects even though they are getting
                                        very high doses of chemo directly into the tumor.&nbsp;</span></p>
                                <p dir="ltr"><span>Embolization and ablation can also work together. For
                                        example, if there is a large tumor, it might need to be embolized first
                                        to block its blood supply and shrink it so that it can then be more
                                        easily burned with ablation. By decreasing the blood flow first,
                                        embolization stops the blood from carrying heat away from the tumor and
                                        allows the ablation to reach higher temperatures to better kill larger
                                        tumors.</span></p>
                                <p dir="ltr"><span>Prostate cancer can also be treated using embolization. By
                                        shrinking the prostate and moving it away from the rectum and other
                                        delicate structures, the embolization allows external beam radiation to
                                        cure the prostate cancer without damaging the rectum or other structures
                                        nearby. In addition, by shrinking the prostate's total volume, the total
                                        radiation dose needed for treatment can also be reduced.</span></p>
                                <p dir="ltr"><span>Embolization can be used to treat not only cancer but also
                                        benign lesions in different organs.&nbsp; In the kidney, embolization
                                        can be used to treat benign tumors called angiomyolipomas that can
                                        otherwise cause internal bleeding. Prostate artery embolization has
                                        become a popular treatment to shrink the prostate in patients with
                                        trouble urinating due to benign prostatic hyperplasia (BPH). Uterine
                                        Fibroid Embolization is another treatment that can help patients with
                                        heavy menstrual bleeding or pain related to fibroids, adenomyosis, or
                                        endometriosis.&nbsp;</span></p>
                                <p dir="ltr"><span>Embolization and ablation are amazing procedures with various
                                        uses that can really help our patients.&nbsp; Advancements in
                                        embolization and ablation are occurring all the time, leading to new and
                                        improved types of treatments.</span></p>
