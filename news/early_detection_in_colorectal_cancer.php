                                <h1>Early Detection in Colorectal Cancer</h1>
                                <div class="post-meta">
                                    <span class="post-meta-date"><i class="fa fa-calendar-o"></i>March 6,
                                        2024</span>
                                </div>
                                <p dir="ltr"><span>March kicks off Colorectal Cancer Awareness Month, amplifying
                                        the conversation around </span><span>Colorectal Cancer. During this
                                        time, it&rsquo;s important to recognize colorectal cancer screening
                                        guidelines and ways to reduce your risk. Also called colon cancer or
                                        rectal cancer, colorectal cancer is a disease where cancer cells form in
                                        the tissues of the colon or the rectum at the end of the digestive
                                        tract. It can take many years for symptoms to appear, which is why early
                                        detection is key in the fight against colorectal cancer.&nbsp;</span>
                                </p>
                                <p dir="ltr"><strong>When to start screening?&nbsp;</strong></p>
                                <ul>
                                    <li dir="ltr">
                                        <p dir="ltr"><span>It&rsquo;s recommended that you start regular
                                                screening for colorectal cancer at age 45.&nbsp;</span></p>
                                    </li>
                                    <li dir="ltr">
                                        <p dir="ltr"><span>You might need to be screen</span><span>ed before age
                                                45 if you have an increased risk of developing colorectal
                                                cancer, like a family history of colorectal cancer, a personal
                                                history of inflammatory bowel disease, ulcerative colitis,
                                                Crohn&rsquo;s disease, or other risk factors.&nbsp;</span></p>
                                    </li>
                                    <li dir="ltr">
                                        <p dir="ltr"><span>You should continue regular colorectal cancer
                                                screening through the age of 75 if you&rsquo;re healthy and have
                                                a life expectancy of 10+ years.</span></p>
                                    </li>
                                    <li dir="ltr">
                                        <p dir="ltr"><span>If you&rsquo;re between ages 76-85, factors like life
                                                expectancy, overall health, and prior screening history
                                                determine your doctor&rsquo;s recommendation about if and when
                                                you should get screened.&nbsp;</span></p>
                                    </li>
                                    <li dir="ltr">
                                        <p dir="ltr"><span>If you&rsquo;re over age 85, you likely won&rsquo;t
                                                need a colorectal cancer screening.</span></p>
                                    </li>
                                </ul>
                                <p dir="ltr"><span>There are several screening tests used to detect colorectal
                                        cancer. Colonoscopies are the most common type of colorectal cancer
                                        screening. Consult your healthcare provider about which test is right
                                        for you.</span></p>
                                <p dir="ltr"><strong>Screening Test Options</strong></p>
                                <p dir="ltr"><strong>Stool Tests</strong></p>
                                <ul>
                                    <li dir="ltr">
                                        <p dir="ltr"><span>The guaiac-based fecal occult blood test (gFOBT) uses
                                                the chemical guaiac to detect blood in the stool. Your
                                                healthcare provider will send you an at-home test kit. The
                                                process of using the at-home test kit includes using a stick or
                                                brush to obtain a small amount of stool. Once this is completed,
                                                you&rsquo;ll return the test kit to your doctor or a lab, where
                                                the stool samples are analyzed for blood.</span></p>
                                    </li>
                                    <ul>
                                        <li dir="ltr">
                                            <p dir="ltr"><span>How often: Once every year</span></p>
                                        </li>
                                    </ul>
                                    <li dir="ltr">
                                        <p dir="ltr"><span>The fecal immunochemical test (FIT) uses antibodies
                                                to detect blood in the stool.</span></p>
                                    </li>
                                    <ul>
                                        <li dir="ltr">
                                            <p dir="ltr"><span>How often: Once every year</span></p>
                                        </li>
                                    </ul>
                                    <li dir="ltr">
                                        <p dir="ltr"><span>The FIT-DNA test (also called a stool DNA test)
                                                combines the FIT with a test that detects altered DNA in the
                                                stool. The process for this test includes collecting an entire
                                                bowel movement and sending it to a lab. The lab will test for
                                                altered DNA and blood.</span></p>
                                    </li>
                                    <ul>
                                        <li dir="ltr">
                                            <p dir="ltr"><span>How often: Once every year</span></p>
                                        </li>
                                    </ul>
                                </ul>
                                <p dir="ltr"><strong>Flexible Sigmoidoscopy</strong></p>
                                <ul>
                                    <li dir="ltr">
                                        <p dir="ltr"><span>During this test, a short, thin, flexible, tube with
                                                a light will be placed into your rectum. Your doctor will look
                                                for polyps or cancer inside the rectum and lower third of the
                                                colon.</span></p>
                                    </li>
                                    <ul>
                                        <li dir="ltr">
                                            <p dir="ltr"><span>How often: Every 5 years, or every 10 years with
                                                    a FIT every year</span></p>
                                        </li>
                                    </ul>
                                </ul>
                                <p dir="ltr"><strong>Colonoscopy</strong></p>
                                <ul>
                                    <li dir="ltr">
                                        <p dir="ltr"><span>A colonoscopy examines the inner lining of the colon
                                                and rectum using a flexible tube with a camera attached. It is
                                                known as a colonoscope and is typically administered to look for
                                                cancer in people who don&rsquo;t have symptoms. If any polyps or
                                                abnormalities are found, they may be removed or biopsied during
                                                the procedure. The procedure usually takes about 30 to 60
                                                minutes. The colonoscopy can be used as a follow-up test if
                                                anything unusual is detected during a stool test or flexible
                                                sigmoidoscopy test.&nbsp;</span></p>
                                    </li>
                                    <ul>
                                        <li dir="ltr">
                                            <p dir="ltr"><span>How often: A normal colonoscopy means you can
                                                    wait another 5-10 years to schedule your next colonoscopy,
                                                    depending on what your doctor recommends.</span></p>
                                        </li>
                                    </ul>
                                </ul>
                                <p dir="ltr"><strong>CT Colonography (Virtual Colonoscopy)</strong></p>
                                <ul>
                                    <li dir="ltr">
                                        <p dir="ltr"><span>Computed tomography (CT) colonography, also called a
                                                virtual colonoscopy, uses X-rays and computers to produce images
                                                of the entire colon.</span></p>
                                    </li>
                                    <ul>
                                        <li dir="ltr">
                                            <p dir="ltr"><span>How often: Every 5 years</span></p>
                                        </li>
                                    </ul>
                                </ul>
                                <p dir="ltr"><strong>The Importance of Colonoscopies</strong></p>
                                <p dir="ltr"><span>As the most common type of colorectal cancer screening,
                                        colonoscopies examine the colon for inflammation, abnormalities, like
                                        polyps, and other signs of colorectal cancer. The majority of colorectal
                                        cancers begin as abnormal growths known as precancerous polyps in the
                                        colon or rectum, and can take 10 to 15 years to develop into cancer.
                                        Because these polyps have a slow cancer growth rate and can present no
                                        symptoms, regular colorectal cancer screening, especially through
                                        colonoscopies, is crucial for removing precancerous polyps before they
                                        turn into colorectal cancer or early detection of colorectal cancer if
                                        it&rsquo;s present. Colorectal cancer can be easier to treat when
                                        it&rsquo;s found early, small, and hasn&rsquo;t spread.&nbsp;
                                        &nbsp;</span></p>
